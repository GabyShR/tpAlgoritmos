void printJuegoMatriz() 
{
	restaurarIdiomaOriginal();

	for (size_t i = 0; i < 40; i++)
	{
		for (size_t j = 0; j < 120; j++)
		{
			setxy(j, i);

			if (Juego[i][j] == 0) color(11); cout << char(219);
			if (Juego[i][j] == 1) color(14); cout << char(219);
			if (Juego[i][j] == 2) color(4); cout << char(219);
			if (Juego[i][j] == 3) color(10); cout << char(219);
			if (Juego[i][j] == 4) color(12); cout << char(219);
			if (Juego[i][j] == 7) color(0); cout << char(219);

		}
	}
}

