void CCreditos::animarNombres()
{
	short diferencia = 3;
	short limiteY = 16;

	bool jahazielBool = true;
	bool dayroBool = false;
	bool gabyBool = false;

	while (true)
	{
		if (jahazielBool)
		{
			jahaziel->borrar();
			jahaziel->mover();
			jahaziel->dibujarJahaziel();

			if (jahaziel->getY() == limiteY)
			{
				jahazielBool = false;
				dayroBool = true;
			}
		}

		if (dayroBool)
		{
			dayro->borrar();
			dayro->mover();
			dayro->dibujarDayro();

			if (dayro->getY() == limiteY + jahaziel->getAlto() + diferencia)
			{
				dayroBool = false;
				gabyBool = true;
			}
		}

		if (gabyBool)
		{
			gabriela->borrar();
			gabriela->mover();
			gabriela->dibujarGabriela();


			if (gabriela->getY() == limiteY + jahaziel->getAlto() + diferencia + dayro->getAlto() + diferencia)
			{
				gabyBool = false;
				break;
			}
		}

		_sleep(10);
	}

}


